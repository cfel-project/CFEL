package cfel.servlet;

import java.io.IOException;

import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import cfel.filter.AuthenticationFilter;

/**
 * Servlet implementation class IsAdministratorServlet
 */
@WebServlet("/cfel_service/is_administrator")
public class IsAdministratorServlet extends HttpServlet {
	private static final long serialVersionUID = 1L;

	/**
	 * @see HttpServlet#doGet(HttpServletRequest request, HttpServletResponse
	 *      response)
	 */
	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		response.setStatus(AuthenticationFilter.isAdministrator(request) ? HttpServletResponse.SC_OK : HttpServletResponse.SC_UNAUTHORIZED);
	}

}
